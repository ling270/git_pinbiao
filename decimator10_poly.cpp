Decimator10Poly::Decimator10Poly(const std::vector<double>& coeffs, int D)
    : m_D(D)
{
    if (coeffs.empty()) {
        m_h.assign(std::begin(DECIM_COEFFS), std::end(DECIM_COEFFS));
    } else {
        m_h = coeffs;
    }
    m_state.assign(m_h.size() > 0 ? (m_h.size() - 1) : 0, 0.0);
}

void Decimator10Poly::processBlock(const std::vector<double>& in, std::vector<double>& out)
{
    if (m_h.empty()) return;
    const size_t M = m_h.size();

    // state + in
    std::vector<double> buf;
    buf.reserve(m_state.size() + in.size());
    buf.insert(buf.end(), m_state.begin(), m_state.end());
    buf.insert(buf.end(), in.begin(), in.end());

    if (buf.size() < M) {
        // 不足以产生输出，只更新状态
        m_state = buf;
        if (m_state.size() > M - 1) {
            m_state.erase(m_state.begin(), m_state.end() - (M - 1));
        }
        return;
    }

    const size_t nOut = (buf.size() - M) / m_D + 1;
    out.reserve(out.size() + nOut);

    for (size_t k = 0; k < nOut; ++k) {
        size_t idx = k * m_D;
        double acc = 0.0;
        for (size_t j = 0; j < M; ++j) {
            acc += m_h[j] * buf[idx + j];
        }
        out.push_back(acc);
    }

    // 更新状态：保留末尾 M-1 点
    m_state.assign(buf.end() - (M - 1), buf.end());
}

void Decimator10Poly::reset()
{
    std::fill(m_state.begin(), m_state.end(), 0.0);
}
